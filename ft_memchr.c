#include <stdlib.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*ptr;

	i = 0;
	ptr = (unsigned char *)s;
	while (i < n && ptr[i] != c)
		i++;
	if (ptr[i] == c)
		return ((void *)&ptr[i]);
	return (NULL);
}
